public class Elek {
    int csp;
    double ertek;

    public Elek(int csp, double ertek) {
        this.csp = csp;
        this.ertek = ertek;
    }

    public int getCsp() {
        return csp;
    }

    public void setCsp(int csp) {
        this.csp = csp;
    }

    public double getErtek() {
        return ertek;
    }

    public void setErtek(double ertek) {
        this.ertek = ertek;
    }
}
